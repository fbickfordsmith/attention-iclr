"""
Combine results files generated by `attention_networks_testing.py` on separate
GPUs.
"""

type_task_set = input('Task-set type in {diff, size, sim}: ')
version_weights = input('Version number (weights): ')

import os
import pandas as pd
from ..utils.paths import path_results

id_weights = f'{type_task_set}_v{version_weights}'
filenames = sorted([f for f in os.listdir(path_results) if id_weights in f])
df = pd.concat(
    [pd.read_csv(path_results/f, index_col=0) for f in filenames],
    ignore_index=True)
df.to_csv(path_results/f'{id_weights}_results.csv')
